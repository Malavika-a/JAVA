public class Operation{
	public static void main(String args[]){
		int a=10;
		int b=10;
		int c=a+b;
		int d=a-b;
		int e=a*b;
		int f=a/b;
		
		System.out.println("Addition a+b:"+c);
		System.out.println("Subtraction a-b:"+d);
		System.out.println("Multiplication a*b:"+e);
		System.out.println("Division a/b:"+f);
		
	}
}